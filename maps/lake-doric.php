			<span style = "font-weight: bold;"> If controlled by Seraph: </span><br> There are two different events that happen simultaneously. 

			<br><br> 1) Destroying 6 White Mantle Catapults. There are 6 champions at each that die very quickly. Unfortunatly, the catapults only take physical damage so conditions are not recommended. 

			<br><br> 2) Destroying Siegemaster Immelhoof. The legendary boss spawns at the enterence of the centaur camp (where the Leather Farm would begin). The goal is to not let the boss reach the fort. If so, the meta fails.

			<br><br> A good method of doing this meta with a group is to cleave the champions defending the catapult then attacking the boss. This way, the catapaults will be temporarily disabled since there will be no one occupying it until the next mob spawns and the focus of the boss will prevent it from failing the meta. Also, champion bags! 

